export function glitchify(text: string) {
  const glitchChars = [
    "©",
    "­",
    "瞞",
    "ȯ",
    "z",
    "¢",
    "u",
    "ꨦ",
    "¸",
    "¯",
    "i",
    "́",
    "º",
    ")",
    "]",
    "³",
    "ٲ",
    "O",
    "±",
    "宮",
    "+",
    "r",
    "µ",
    "릊",
    "%",
    "¢",
    "ل",
    "ك",
    "ش",
    "忍",
    "密",
    "藏",
    "技",
    "術",
    "꧑",
    "Ϟ",
    "ϟ",
    "҉",
    "҈",
    "ҋ",
    "Ҍ",
    "ґ",
    "Ҕ",
    "",
    "܊",
    "",
    "©",
    "¢",
    "w",
    "§",
    "¶",
    "Ï",
    "Ͼ",
    "Ҝ",
  ]

  return text
    .split("")
    .map(() => glitchChars[Math.floor(Math.random() * glitchChars.length)])
    .join("")
}
